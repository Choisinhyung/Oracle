-- DEPARTMENTS 테이블에서 DEPARTMENT_NAME이 Marketing 인 데이터 조회
SELECT * FROM DEPARTMENTS
 WHERE DEPARTMENT_NAME LIKE 'Marketing';

-- DEPARTMENTS 테이블에서 DEPARTMENT_NAME이 IT가 포함된 데이터 조회
SELECT * FROM DEPARTMENTS
 WHERE DEPARTMENT_NAME LIKE '%IT%';

-- LOCATIONS 테이블에서 COUNTRY_ID가 US인 데이터 조회할 때, STREET_ADDRESS, POSTAL_CODE, CITY 컬럼만 조회한다
SELECT STREET_ADDRESS
	 , POSTAL_CODE
	 , CITY 
  FROM LOCATIONS
 WHERE COUNTRY_ID LIKE 'US';

